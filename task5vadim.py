import math


def main(z, x, y):
    n = len(z)
    summation = 0
    for i in range(1, n+1):
        z1 = z[i-1]**2
        z2 = x[n-math.ceil(i/3)]
        z3 = y[math.ceil(i/2)-1]**3
        mc = math.acos(z1 + z2 + z3)
        summation += (mc**4)/70
    return summation * 87

